t=0
id=0

function TIC()

	if btnp(0) then sfx(id,"E-4")
	elseif btnp(1) then sfx(id,"E-6")
	elseif btnp(2) then sfx(id,"E-7")
	elseif btnp(3) then sfx(id,"E-5")
	end

	cls(15)
	spr(1+(t%20)/10,102,24,1,4)
	print("PRESS ANY ARROW KEY",64,64,0)
	
	t=t+1
	
end

